import numpy as np

def KapurRoklin(f,a,b,N,m,y1,y2):
    h = (b-a)/(N-1)
    v1 = np.zeros(2*m)
    v2 = np.zeros(2*m)
    for i in range(-m,m):
        if i != 0:
            if (np.isnan(f(i*h))==True and np.isnan(f(i*h + b)==True)):
                #v1[i + m] = y1[i + m]*f(h)
                v1[i + m] = 0
                v2[i + m] = 0
            elif np.isnan(f(i*h))==True:
                v1[i + m] = 0
                v2[i + m] = y2[i + m]*f(i*h + b)
            elif np.isnan(f(i*h + b))==True:
                v1[i + m] = y1[i + m]*f(i*h)
                v2[i + m] = 0
            else:
                v1[i + m] = y1[i + m]*f(i*h)
                v2[i + m] = y2[i + m]*f(i*h + b)

    area = 0
    for i in range(1,N-2):
        area = area + f(i*h)
    area = h*(area + sum(v1) + sum(v2))
    return(area)
    
